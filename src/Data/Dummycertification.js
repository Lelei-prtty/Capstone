// Dynamic match score based on recommended role
// Primary match = 95-99%, secondary = 70-85%, unrelated = 40-60%
export function getMatchScore(cert, recommendedRole) {
  if (!recommendedRole) return cert.matchScore
  if (cert.roles.includes(recommendedRole)) {
    // Primary match — use the cert's own matchScore (already high)
    return cert.matchScore
  }
  // Not a match for this role — lower the score significantly
  return Math.floor(40 + Math.random() * 20)
}

// Get certs sorted by relevance to the recommended role
export function getSortedCerts(recommendedRole) {
  if (!recommendedRole) return certifications
  return [...certifications].sort((a, b) => {
    const aMatch = a.roles.includes(recommendedRole)
    const bMatch = b.roles.includes(recommendedRole)
    if (aMatch && !bMatch) return -1
    if (!aMatch && bMatch) return 1
    return b.matchScore - a.matchScore
  })
}