// Helper: get certifications for a specific recommended role, sorted by matchScore
export function getCertsByRole(recommendedRole) {
  if (!recommendedRole) return certifications
  const matched = certifications
    .filter((c) => c.roles.includes(recommendedRole))
    .sort((a, b) => b.matchScore - a.matchScore)
  const others = certifications
    .filter((c) => !c.roles.includes(recommendedRole))
    .sort((a, b) => b.matchScore - a.matchScore)
  return [...matched, ...others]
}

